  // Helper function to categorize a product title
  categorize(title) {
    if (!title) return 'อื่นๆ';
    const t = title.toLowerCase();

    // Loop through categories and find first match
    for (const [categoryName, keywords] of Object.entries(this.categories)) {
      if (keywords.length === 0) continue;
      if (keywords.some(keyword => t.includes(keyword.toLowerCase()))) {
        return categoryName;
      }
    }

    return 'อื่นๆ';
  }
