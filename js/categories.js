  /**
   * Categorize a product title into the hierarchical tree
   */
  categorize(title) {
    if (!title) return 'อื่นๆ';
    const t = title.toLowerCase();

    // Helper to check if a keyword matches correctly (whole word for EN, substring for TH)
    const checkMatch = (text, kw) => {
      const k = kw.toLowerCase();
      // If keyword is purely English/Numbers, use word boundaries
      if (/^[a-z0-9\s\-]+$/i.test(k)) {
        const regex = new RegExp(`(^|\\s|[^a-z0-9])${k}($|\\s|[^a-z0-9])`, 'i');
        return regex.test(text);
      }
      // For Thai/Mixed, use includes
      return text.includes(k);
    };

    // 1. Try to find by specific sub-category keywords
    for (const [subCat, keywords] of Object.entries(this.keywords)) {
      if (subCat === '_major') continue;
      if (keywords.some(kw => checkMatch(t, kw))) {
        // Find the major category for this sub
        for (const [major, subs] of Object.entries(this.tree)) {
          if (subs.includes(subCat)) {
            return `${major} > ${subCat}`;
          }
        }
        return subCat;
      }
    }

    // 2. Try to find by major category keywords
    for (const [major, keywords] of Object.entries(this.keywords._major)) {
      if (keywords.some(kw => checkMatch(t, kw))) {
        return major;
      }
    }

    // 3. Last resort: Check if any major category name is in the title
    for (const major of Object.keys(this.tree)) {
      if (t.includes(major.toLowerCase())) return major;
    }

    return 'ทั่วไป';
  },

  /**
   * Batch categorize
   */
  categorizeBatch(products) {
    return products.map(p => ({
      ...p,
      category: this.categorize(p.title)
    }));
  }
